  function normalizeLanguage(lang) {
    return SUPPORTED.includes(lang) ? lang : DEFAULT_LANGUAGE;
  }

  function getLanguage() {
    try {
      return normalizeLanguage(sessionStorage.getItem(STORAGE_KEY) || DEFAULT_LANGUAGE);
    } catch (error) {
      return DEFAULT_LANGUAGE;
    }
  }

  function setLanguage(lang) {
    const selected = normalizeLanguage(lang);
    try {
      sessionStorage.setItem(STORAGE_KEY, selected);
    } catch (error) {
      // Ignore storage failures and keep the in-memory/default behavior.
    }
    document.documentElement.lang = selected;
    document.documentElement.dataset.language = selected;
    applyTranslations(document);
    window.dispatchEvent(new CustomEvent("kmm:languagechange", { detail: { language: selected } }));
    return selected;
  }

  function t(key) {
    const lang = getLanguage();
    return translations[lang]?.[key] || translations.en[key] || key;
  }

  function translateNode(node) {
    if (!node || node.nodeType !== Node.ELEMENT_NODE) return;

    const textKey = node.dataset.i18n || staticTextKeys[node.textContent.trim()];
    if (textKey && node.childElementCount === 0) {
      node.textContent = t(textKey);
    }

    const htmlKey = node.dataset.i18nHtml;
    if (htmlKey) node.innerHTML = t(htmlKey);

    const placeholderKey = node.dataset.i18nPlaceholder || staticTextKeys[node.getAttribute("placeholder") || ""];
    if (placeholderKey) node.setAttribute("placeholder", t(placeholderKey));

    const ariaKey = node.dataset.i18nAria || staticTextKeys[node.getAttribute("aria-label") || ""];
    if (ariaKey) node.setAttribute("aria-label", t(ariaKey));

    const titleKey = node.dataset.i18nTitle || staticTextKeys[node.getAttribute("title") || ""];
    if (titleKey) node.setAttribute("title", t(titleKey));
  }

  function applyTranslations(root) {
    const scope = root || document;
    if (scope.nodeType === Node.ELEMENT_NODE) translateNode(scope);
    scope.querySelectorAll?.("[data-i18n], [data-i18n-html], [data-i18n-placeholder], [data-i18n-aria], [data-i18n-title], h1, h2, h3, p, span, strong, small, label, button, a, option, th, td, em, div").forEach(translateNode);
    document.documentElement.lang = getLanguage();
    document.documentElement.dataset.language = getLanguage();
  }

  window.KMMI18n = {
    applyTranslations,
    getLanguage,
    setLanguage,
    t,
    translations
  };

